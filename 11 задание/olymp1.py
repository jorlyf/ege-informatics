# При регистрации в компьютерной системе каждому пользователю выдаётся пароль,
# состоящий из 11 символов и содержащий только символы из 12-символьного набора:
# А, В, C, D, Е, F, G, H, K, L, M, N. В базе данных для хранения сведений
# о каждом пользователе отведено одинаковое и минимально возможное целое число байт.
# При этом используют посимвольное кодирование паролей,
# все символы кодируют одинаковым и минимально возможным количеством бит.
