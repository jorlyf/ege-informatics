"""
при регистрации в комп системе каждому пользователю выдают пароль: 11 символов.
пароль содержит прописные и строчные латинские буквы, имеет не менее одного из символов: "# & $ * ! @".
для хранений сведение для каждого пользователя минимум возможное число байт.
+ доп сведения на целое число байт, для хранения 25 пользователей нужно 500 байт.
определите сколько байт занимают доп сведения на каждого пользователя.
"""
# 26 букв в латинском алфавите
