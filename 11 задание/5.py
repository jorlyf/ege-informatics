один_пароль_бит = 11 * один_символ_бит
один_пароль_байт = один_пароль_бит // 8 + 1
объем_паролей_25_пользователей_байт = 25 * один_пароль_байт # 200
print((500 - объем_паролей_25_пользователей_байт) // 25)

